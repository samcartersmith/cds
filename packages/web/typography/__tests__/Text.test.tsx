import { render, screen } from '@testing-library/react';
import { ResponsiveProps, ResponsivePropsDevices } from '@cbhq/cds-common';
import { variantsWithTransforms } from '@cbhq/cds-common/hooks/useTextTransform';
import type { Typography } from '@cbhq/cds-common/types';
import { renderA11y } from '@cbhq/cds-web-utils/jest';

import { responsiveClassName } from '../../styles/responsive';
import { ThemeProvider } from '../../system';
import { DynamicElement } from '../../types';
import {
  TextBody,
  TextCaption,
  TextDisplay1,
  TextDisplay2,
  TextDisplay3,
  TextHeadline,
  TextLabel1,
  TextLabel2,
  TextLegal,
  TextTitle1,
  TextTitle2,
  TextTitle3,
  TextTitle4,
} from '../index';
import { HTMLNonHeadingTextTags, HTMLTextTags, TextProps } from '../TextProps';

const textComponents = [
  TextDisplay1,
  TextDisplay2,
  TextDisplay3,
  TextTitle1,
  TextTitle2,
  TextTitle3,
  TextTitle4,
  TextHeadline,
  TextBody,
  TextLabel1,
  TextLabel2,
  TextCaption,
  TextLegal,
] as const;

type TextComponent =
  | React.ComponentType<React.PropsWithChildren<DynamicElement<TextProps, HTMLTextTags, true>>>
  | React.ComponentType<
      React.PropsWithChildren<DynamicElement<TextProps, HTMLNonHeadingTextTags, true>>
    >;

const textTestRunner = (testFn: (type: TextComponent) => void) => {
  textComponents.map(async (TextComponent) => testFn(TextComponent));
};

function expectClassName<K extends keyof TextProps>(
  TextComponent: TextComponent,
  prop: K,
  values: NonNullable<TextProps[K]>[],
  otherClasses?: string,
) {
  values.forEach((value) => {
    // eslint-disable-next-line jest/require-top-level-describe
    it(`${TextComponent.displayName} will set "${value}" class name for \`${prop}\` prop`, () => {
      render(
        <TextComponent as="p" {...{ [prop]: value }}>
          Child
        </TextComponent>,
      );
      expect(screen.getByText('Child')).toHaveClass(
        otherClasses ? `${otherClasses} ${value}` : String(value),
      );
    });
  });
}

describe('Text', () => {
  textTestRunner((TextComponent) =>
    it(`${TextComponent.displayName} passes accessibility`, async () => {
      expect(await renderA11y(<TextComponent as="span">Child</TextComponent>)).toHaveNoViolations();
    }),
  );

  textTestRunner((TextComponent) =>
    (
      [
        'p',
        'span',
        'label',
        'time',
        'output',
        'code',
        'pre',
        's',
        'abbr',
        'q',
        'kbd',
        'del',
        'ins',
        'sup',
        'sub',
        'li',
      ] as const
    ).forEach((tag) => {
      it(`${TextComponent.displayName} can render as a "${tag}" element using the \`as\` prop`, () => {
        const { container } = render(<TextComponent as={tag}>Child</TextComponent>);

        // This requires DOM traversing to confirm specific tags are generated by Text.
        // - Emily Seibert, 10/25/2022
        // eslint-disable-next-line testing-library/no-container, testing-library/no-node-access
        expect(container.querySelectorAll(tag)).toHaveLength(1);
      });
    }),
  );

  it('can show tabular numbers', () => {
    textTestRunner((TextComponent) => {
      render(
        <TextComponent tabularNumbers as="p">
          20.21
        </TextComponent>,
      );

      expect(screen.getAllByText('20.21')[0]).toHaveClass('tabularNumbers');
    });
  });

  it('can show slashed Zero', () => {
    textTestRunner((TextComponent) => {
      render(
        <TextComponent slashedZero as="p">
          YO2021Coinbase
        </TextComponent>,
      );

      expect(screen.getAllByText('YO2021Coinbase')[0]).toHaveClass('slashedZero');
    });
  });

  it('can show underline', () => {
    textTestRunner((TextComponent) => {
      render(
        <TextComponent underline as="p">
          Underline
        </TextComponent>,
      );

      expect(screen.getAllByText('Underline')[0]).toHaveClass('underline');
    });
  });

  it('can be styled to not wrap', () => {
    textTestRunner((TextComponent) => {
      render(
        <TextComponent noWrap as="p">
          No Wrap
        </TextComponent>,
      );

      expect(screen.getAllByText('No Wrap')[0]).toHaveClass('noWrap');
    });
  });
  describe('overflow', () => {
    textTestRunner((TextComponent) => {
      expectClassName(TextComponent, 'overflow', ['truncate', 'clip']);
    });
  });

  describe('selectable', () => {
    textTestRunner((TextComponent) => {
      expectClassName(TextComponent, 'selectable', ['none', 'text', 'all']);
    });
  });

  describe('transform works with any override', () => {
    textTestRunner((TextComponent) => {
      expectClassName(TextComponent, 'transform', ['uppercase', 'lowercase', 'capitalize', 'none']);
    });
  });

  describe('transform defaults in normal', () => {
    textTestRunner((TextComponent) => {
      const name = TextComponent.displayName?.replace('Text', '').toLowerCase() as Typography;
      if (variantsWithTransforms.normal.has(name)) {
        it(`${name} defaults to uppercase in normal`, () => {
          render(<TextComponent as="p">Child</TextComponent>);
          expect(screen.getByText('Child')).toHaveClass('uppercase');
        });
      }
    });
  });

  describe('transform defaults in dense', () => {
    textTestRunner((TextComponent) => {
      const name = TextComponent.displayName?.replace('Text', '').toLowerCase() as Typography;
      if (variantsWithTransforms.dense.has(name)) {
        it(`${name} defaults to uppercase in dense`, () => {
          render(
            <ThemeProvider scale="xSmall">
              <TextComponent as="p">Child</TextComponent>
            </ThemeProvider>,
          );
          expect(screen.getByText('Child')).toHaveClass('uppercase');
        });
      }
    });
  });

  describe('responsive styles', () => {
    const DEFAULT_CLASS = 'typographyResets';

    const getClassNamesForResponsiveProps = (config: ResponsiveProps, style: string) => {
      const classNames: string[] = [];

      classNames.push(DEFAULT_CLASS);

      (Object.keys(config) as ResponsivePropsDevices[]).forEach((device) => {
        // @ts-expect-error PITA to type this
        classNames.push(config[device]?.[style] as string);
      });

      classNames.push(responsiveClassName);

      return classNames.join(' ');
    };

    const responsiveFlexStylesConfig: ResponsiveProps = {
      phone: {
        justifyContent: 'flex-start',
      },
      tablet: {
        justifyContent: 'space-around',
      },
      desktop: {
        justifyContent: 'flex-end',
      },
    };

    it('renders flex classNames for styles at each device breakpoint', () => {
      render(
        <TextTitle1 as="h1" responsiveConfig={responsiveFlexStylesConfig}>
          Child
        </TextTitle1>,
      );
      const classNames = getClassNamesForResponsiveProps(
        responsiveFlexStylesConfig,
        'justifyContent',
      );

      expect(screen.getByText('Child')).toHaveClass(classNames);
    });

    const responsiveVisibilityStylesConfig: ResponsiveProps = {
      phone: {
        visibility: 'hidden',
      },
      tablet: {
        visibility: 'visible',
      },
    };

    it('renders visibility classNames for styles at each device breakpoint', () => {
      render(
        <TextTitle1 as="h1" responsiveConfig={responsiveVisibilityStylesConfig}>
          Child
        </TextTitle1>,
      );
      const classNames = getClassNamesForResponsiveProps(
        responsiveVisibilityStylesConfig,
        'visibility',
      );

      expect(screen.getByText('Child')).toHaveClass(classNames);
    });
  });
});
