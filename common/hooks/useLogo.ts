export const useLogoWordmark = ({ foreground }: LogoWordmarkParams) => {
  const spectrum = useSpectrum();
  const wordmarkColorOverride = spectrum === 'light' ? black : white;
  return useMemo(() => {
    return {
      ...wordmarkData,
      color: foreground ? wordmarkColorOverride : blue,
    };
  }, [foreground, wordmarkColorOverride]);
};

export const useLogoMark = ({ size = 32 }: LogoMarkParams) => {
  const spectrum = useSpectrum();
  const viewBox = `0 0 ${size} ${size}`;
  const path = logoMarkData[size];
  const color = size === 16 && spectrum === 'light' ? blue : white;
  const backgroundColor = spectrum === 'light' ? blue : black;
  return useMemo(() => {
    return {
      circle:
        size === 16
          ? undefined
          : {
              radius: size / 2,
              backgroundColor,
            },
      color,
      path,
      viewBox,
      width: size,
      height: size,
    };
  }, [backgroundColor, color, path, size, viewBox]);
};
